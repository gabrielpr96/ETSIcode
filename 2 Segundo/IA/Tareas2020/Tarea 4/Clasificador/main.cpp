void dividir(vector<Ejemplo> ejemplos, vector<string> atributos){
    vector<string> clases;
    for(unsigned int e = 0; e < ejemplos.size(); e++){
        if(find(clases.begin(), clases.end(), ejemplos[e].clase) == clases.end()){
            clases.push_back(ejemplos[e].clase);
        }

    }
    if(clases.size() == 0){
        cout << "No hay ejemplos" << endl; //Creo que no deberia ocurrir
        return;
    }else if(clases.size() == 1){
        cout << "Hoja de clase: " << clases[0] << endl;
        return;
    }

    string mejorAtributo = "";
    double mejorEntropia = -1;
    unsigned int mejorAtributoPos = -1;
    map<string, vector<string> > valores; //Atributo -> [Valor]
    for(unsigned int a = 0; a < atributos.size(); a++){
        for(unsigned int e = 0; e < ejemplos.size(); e++){
            if(find(valores[atributos[a]].begin(), valores[atributos[a]].end(), ejemplos[e].atributos[atributos[a]]) == valores[atributos[a]].end()){
                valores[atributos[a]].push_back(ejemplos[e].atributos[atributos[a]]);
            }
        }

        double entropiaAtributo = 0;
        for(unsigned int v = 0; v <  valores[atributos[a]].size(); v++){
            int ejemplosValorConcreto = 0;
            for(unsigned int e = 0; e < ejemplos.size(); e++){
                if(ejemplos[e].atributos[atributos[a]] == valores[atributos[a]][v])
                    ejemplosValorConcreto++;
            }

            double entropiavalor = 0;
            for(unsigned int c = 0; c < clases.size(); c++){
                int ejemplosValorConcretoClase = 0;
                for(unsigned int e = 0; e < ejemplos.size(); e++){
                    if(ejemplos[e].atributos[atributos[a]] == valores[atributos[a]][v] && ejemplos[e].clase == clases[c])
                        ejemplosValorConcretoClase++;
                }
                double frac = ejemplosValorConcretoClase/(double)ejemplosValorConcreto;
                //cout << "Frac de " << clases[c] << ": " << ejemplosValorConcretoClase << "/" << ejemplosValorConcreto << "=" << frac << endl;
                if(ejemplosValorConcretoClase != 0)
                    entropiavalor -= frac*log2(frac);
            }
            //cout << "Valor previo " << entropiavalor << " Mul " << ejemplosValorConcreto << " / " << ejemplos.size() << endl;
            entropiaAtributo += (ejemplosValorConcreto/(double)ejemplos.size())*entropiavalor;
        }
        //cout << "Atributo: " << atributos[a] << " entropia: " << entropiaAtributo << endl;
        if(mejorEntropia < 0 || entropiaAtributo < mejorEntropia){
            mejorEntropia = entropiaAtributo;
            mejorAtributo = atributos[a];
            mejorAtributoPos = a;
        }
    }
    //cout << "Mejor atributo: " << mejorAtributo << " entropia: " << mejorEntropia << endl;
    atributos.erase(atributos.begin() + mejorAtributoPos);
    for(unsigned int v = 0; v < valores[mejorAtributo].size(); v++){
        vector<Ejemplo> subEjemplos;
        for(unsigned int e = 0; e < ejemplos.size(); e++){
            if(ejemplos[e].atributos[mejorAtributo] == valores[mejorAtributo][v])
                subEjemplos.push_back(ejemplos[e]);
        }
        cout << "Subarbol " << mejorAtributo << " valor " << valores[mejorAtributo][v] << endl;
        dividir(subEjemplos, atributos);
        cout << "Fin de " << mejorAtributo << " valor " << valores[mejorAtributo][v] << endl;
    }
    //cout << "Fin de " << mejorAtributo << endl;
}

