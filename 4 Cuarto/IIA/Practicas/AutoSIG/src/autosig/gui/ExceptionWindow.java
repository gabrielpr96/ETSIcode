    public ExceptionWindow(SIGException exception) {
        this.exception = exception;
        initComponents();

        fieldMessage.setText(exception.getMessage());

        Object obj = exception.getAssociatedObject();
        if (obj != null) {
            fieldObject.setText(obj.toString());
        }

        Exception ex = exception.getNestedException();
        if (ex != null) {
            StringWriter sw = new StringWriter();
            PrintWriter pw = new PrintWriter(sw);
            ex.printStackTrace(pw);
            fieldException.setText(sw.toString());
        }
    }

